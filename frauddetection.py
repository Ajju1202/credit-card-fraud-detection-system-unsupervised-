from sklearn.model_selection import train_test_split # Import train_test_split for splitting data into training and testing sets.
from sklearn.preprocessing import StandardScaler # Import StandardScaler for feature scaling.

# Separate features and labels
X = df.drop(columns=['Class']) # Create feature set 'X' by dropping the 'Class' column from the DataFrame.
y = df['Class'] # Create target variable 'y' with the 'Class' column.

# Scale features
scaler = StandardScaler() # Initialize StandardScaler.
X_scaled = scaler.fit_transform(X) # Fit the scaler to the features and transform them to have zero mean and unit variance.

# Train-test split (stratified to preserve fraud ratio)
X_train, X_test, y_train, y_test = train_test_split( # Split the scaled features and target variable into training and testing sets.
    X_scaled, y, test_size=0.25, stratify=y, random_state=42 # Use 25% of the data for testing, stratify by 'y' to maintain class distribution, and set a random state for reproducibility.
)

from sklearn.metrics import ( # Import various metrics from sklearn.metrics for model evaluation.
    roc_auc_score, # Import ROC AUC score for evaluating binary classifiers.
    average_precision_score, # Import Average Precision score for evaluating binary classifiers, especially useful for imbalanced datasets.
    classification_report, # Import classification_report to display precision, recall, f1-score, and support for each class.
    confusion_matrix # Import confusion_matrix to evaluate the performance of a classification model.
)
import numpy as np # Import the numpy library for numerical operations.

from sklearn.ensemble import IsolationForest # Import IsolationForest, an anomaly detection algorithm.

iso = IsolationForest( # Initialize the IsolationForest model.
    n_estimators=200, # Set the number of base estimators (trees) to 200.
    contamination=y_train.mean(), # Estimate the proportion of outliers in the data, using the fraud fraction from the training set.
    random_state=42 # Set the random state for reproducibility.
)
iso.fit(X_train) # Fit the IsolationForest model to the training data. This step builds the ensemble of isolation trees.

iso_scores = -iso.decision_function(X_test)  # Calculate anomaly scores for the test set. Higher scores indicate a higher likelihood of being anomalous (fraudulent). The negative sign is used because `decision_function` returns lower values for anomalies.
iso_auc = roc_auc_score(y_test, iso_scores) # Calculate the Area Under the Receiver Operating Characteristic Curve (ROC AUC) for Isolation Forest.
iso_ap = average_precision_score(y_test, iso_scores) # Calculate the Average Precision (AP) score for Isolation Forest.

print("Isolation Forest ROC-AUC:", iso_auc) # Print the calculated ROC AUC score.
print("Isolation Forest AP:", iso_ap) # Print the calculated Average Precision score.

threshold = np.percentile(iso_scores, 99.8) # Determine a threshold for anomaly detection by taking the 99.8th percentile of the anomaly scores. Scores above this threshold will be classified as anomalies.
iso_pred = (iso_scores >= threshold).astype(int) # Classify instances as fraudulent (1) if their anomaly score is greater than or equal to the threshold, and non-fraudulent (0) otherwise.

print(confusion_matrix(y_test, iso_pred)) # Print the confusion matrix to evaluate the performance of the Isolation Forest model's binary classification.
print(classification_report(y_test, iso_pred, digits=4)) # Print a detailed classification report including precision, recall, f1-score, and support for each class, with 4 decimal places.

k = 100 # Define 'k' as the number of top anomalous instances to consider.
top_k_idx = np.argsort(iso_scores)[-k:] # Get the indices of the 'k' instances with the highest anomaly scores (most anomalous).
precision_at_100 = y_test.iloc[top_k_idx].sum() / k # Calculate Precision@100: the proportion of actual fraudulent cases among the top 'k' instances identified as anomalous.
print("Isolation Forest Precision@100:", precision_at_100) # Print the calculated Precision@100.

from sklearn.neighbors import LocalOutlierFactor # Import LocalOutlierFactor (LOF), another anomaly detection algorithm.

lof = LocalOutlierFactor( # Initialize the LocalOutlierFactor model.
    n_neighbors=20, # Set the number of neighbors to consider for local density estimation to 20.
    contamination=y_train.mean() # Estimate the proportion of outliers in the data, using the fraud fraction from the training set.
) # Note: LOF is typically unsupervised, but contamination can be used to set a threshold for decision_function.

lof_pred = lof.fit_predict(X_test) # Fit the LOF model to the test data and predict outlier labels (-1 for outlier, 1 for inlier).
lof_scores = -lof.negative_outlier_factor_ # Calculate anomaly scores. Lower negative_outlier_factor_ values indicate higher anomaly likelihood, so we negate it to get higher scores for anomalies.

lof_auc = roc_auc_score(y_test, lof_scores) # Calculate the ROC AUC score for LOF.
lof_ap = average_precision_score(y_test, lof_scores) # Calculate the Average Precision score for LOF.

print("LOF ROC-AUC:", lof_auc) # Print the calculated LOF ROC AUC score.
print("LOF AP:", lof_ap) # Print the calculated LOF Average Precision score.

lof_binary = (lof_pred == -1).astype(int) # Convert LOF predictions to binary: 1 for outliers (-1) and 0 for inliers (1).
