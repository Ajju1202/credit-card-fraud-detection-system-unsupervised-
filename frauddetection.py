from tensorflow.keras import layers, models # Import layers and models from TensorFlow Keras for building neural networks.

input_dim = X_train.shape[1] # Get the number of features (columns) in the training data, which will be the input dimension for the autoencoder.

model = models.Sequential([ # Initialize a Sequential model for building the autoencoder layer by layer.
    layers.Dense(64, activation='relu', input_shape=(input_dim,)), # Add the first dense (fully connected) layer with 64 units, ReLU activation, and specify the input shape.
    layers.Dense(32, activation='relu'), # Add a second dense layer with 32 units and ReLU activation (encoder part).
    layers.Dense(64, activation='relu'), # Add a third dense layer with 64 units and ReLU activation (decoder part).
    layers.Dense(input_dim) # Add the output dense layer with the same number of units as the input dimension, to reconstruct the original input.
])

model.compile(optimizer='adam', loss='mse') # Compile the model: use the Adam optimizer and Mean Squared Error (MSE) as the loss function, as this is a reconstruction task.
model.fit(X_train, X_train, epochs=20, batch_size=256, validation_split=0.1) # Train the autoencoder: fit the model on the training data, using X_train as both input and target for reconstruction. Train for 20 epochs with a batch size of 256, and use 10% of the training data for validation.

recon = model.predict(X_test) # Use the trained autoencoder to predict (reconstruct) the test data.
recon_error = np.mean((X_test - recon) ** 2, axis=1) # Calculate the reconstruction error for each instance in the test set. This is typically the mean squared error between the original and reconstructed data.

